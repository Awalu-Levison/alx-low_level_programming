#include <stdio.h>
/**
 * add - A function that aadd two numbers
 * @a: Fisrt number
 * @b: Second number
 * Return: The sum of two numbers
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - Subtract two numbera
 * @a: First number
 * @b: Second number
 * Return: The difference of two numbers
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - A function that multiplies two numbers
 * @a: First number
 * @b: Second number
 * Return: The product of two numbers
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * mod - A modulus of two numbers
 * @a: First number
 * @b: Second number
 * Return: Modulus of a and b
 */
int mod(int a, int b)
{
	return (a % b);
}
/**
 * div - A division of two numbers
 * @a: First number
 * @b: Second number
 * Return: Division of a and b
 */
int div(int a, int b)
{
	return (a / b);
}
